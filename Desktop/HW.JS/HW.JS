const emailRegex = /^[a-zA-Z0-9]+(?:\.[a-zA-Z0-9]+)*@[a-zA-Z0-9]+(?:\.[a-zA-Z0-9]+)+$/;

const emails = [
    "olenabaranova@example.com",
    "invalid.email",
    "baranova@email.co.uk",
    "olenka.email1981@domain.com"
];

const validEmails = emails.filter(email => emailRegex.test(email));

